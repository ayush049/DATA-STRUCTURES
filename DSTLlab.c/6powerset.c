#include <stdio.h>
#include <math.h>
int main(){
    int a[10];
    int m,n,p=0;
    printf("Enter the size of first array: ");
    scanf("%d",&n);
    printf("Enter elements of the array: ");
    for(int i=0;i<n;i++){
        scanf("%d",&a[i]);
    }
    for(int i=1;i<2*(pow,m*n);i++){
        p++;
    }
    printf("Number of Elements in the Power set is: %d",p);

}